//------------------------------------------------------------
// SlideByteV5
//------------------------------------------------------------

func SlideByteV5(charByte byte) byte {
	//------------------------------------------------------------
	if charByte <= 0x1F {
		return 0x1F - charByte
	} else if charByte <= 0x7E {
		return 0x7E - (charByte - 0x20)
	} else if charByte >= 0x80 {
		return 0xFF - (charByte - 0x80)
	}
	//------------------------------------------------------------
	return charByte
	//------------------------------------------------------------
}


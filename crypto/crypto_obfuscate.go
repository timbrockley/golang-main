//------------------------------------------------------------
// ObfuscateV4
//------------------------------------------------------------

func ObfuscateV4(dataString string) string {
	//------------------------------------------------------------
	if dataString == "" {
		return dataString
	}
	//------------------------------------------------------------
	var dataBytes, strBytes, strXBytes []byte
	var charByte byte
	var length, half int
	//------------------------------------------------------------
	dataBytes = []byte(dataString)
	//------------------------------------------------------------
	if len(dataBytes) >= 4 {
		//--------------------
		length = len(dataBytes)
		half = int(length / 2)
		//--------------------
		if half%2 != 0 {
			half -= 1
			length = half * 2
			strXBytes = dataBytes[length:]
			dataBytes = dataBytes[0:length]
		}
		//--------------------
		for i1 := 0; i1 < len(dataBytes); i1 += 1 {
			//--------------------
			if i1%2 != 0 {
				if i1 < half {
					charByte = dataBytes[i1+half]
				} else {
					charByte = dataBytes[i1-half]
				}
			} else {
				charByte = dataBytes[i1]
			}
			//--------------------
			strBytes = append(strBytes, SlideByteV4(charByte))
			//--------------------
		}

	} else {
		strXBytes = dataBytes
	}
	//------------------------------------------------------------
	if len(strXBytes) > 0 {
		for i2 := 0; i2 < len(strXBytes); i2 += 1 {
			strBytes = append(strBytes, SlideByteV4((strXBytes[i2])))
		}
	}
	//------------------------------------------------------------
	return string(strBytes)
	//------------------------------------------------------------
}

//------------------------------------------------------------
// SlideByteV4
//------------------------------------------------------------

func SlideByteV4(charByte byte) byte {
	//------------------------------------------------------------
	if charByte >= 0x20 && charByte <= 0x7E {
		charByte = 0x9E - charByte
	}
	//------------------------------------------------------------
	return charByte
	//------------------------------------------------------------
}

//------------------------------------------------------------
// ObfuscateV4_encode
//------------------------------------------------------------

func ObfuscateV4_encode(dataString string) string {
	//------------------------------------------------------------
	if dataString == "" {
		return dataString
	}
	//------------------------------------------------------------
	dataString = ObfuscateV4(dataString)
	//------------------------------------------------------------
	replacer := strings.NewReplacer(
		"\\", "\\\\",
		"\x09", "\\t", // tab
		"\x0A", "\\n", // new line
		"\x0D", "\\r", // carriage return
		"\x22", "\\q", // double quote
		"\x27", "\\a", // apostrophy
		"\x60", "\\g", // grave accent
	)
	//------------------------------------------------------------
	return replacer.Replace(dataString)
	//------------------------------------------------------------
}

//------------------------------------------------------------
// ObfuscateV4_decode
//------------------------------------------------------------

func ObfuscateV4_decode(dataString string) string {
	//------------------------------------------------------------
	if dataString == "" {
		return dataString
	}
	//------------------------------------------------------------
	replacer := strings.NewReplacer(
		"\\\\", "\\SUB",
		"\\g", "\x60", // grave accent
		"\\a", "\x27", // apostrophy
		"\\q", "\x22", // double quote
		"\\r", "\x0D", // carriage return
		"\\n", "\x0A", // new line
		"\\t", "\x09", // tab
	)
	//------------------------------------------------------------
	dataString = replacer.Replace(dataString)
	//------------------------------------------------------------
	dataString = strings.Replace(dataString, "\\SUB", "\\", -1)
	//------------------------------------------------------------
	return ObfuscateV4(dataString)
	//------------------------------------------------------------
}

